    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("No.Cotizacion");//ENCABEZADO
        modelo.addColumn("ID proveedor");
        modelo.addColumn("Fecha");
        modelo.addColumn("ID Producto");//DETALLE
        modelo.addColumn("Cantidad");
        modelo.addColumn("Total");
        

        daoCotizacionCompras cotiDAO = new daoCotizacionCompras();
        List<clsCotizacionCompras> cotizacion = cotiDAO.select();
        
        daoCotizacionCompras cotiDAO2 = new daoCotizacionCompras();
        List<clsCotizacionCompras> cotizacion2 = cotiDAO2.select2();
        
        tablaCompras.setModel(modelo);
        String[] dato = new String[10];
        for (int i = 0; i < cotizacion.size(); i++) {
            dato[0] = cotizacion.get(i).getCce_documento();//ENCABEZADO
            dato[1] = Integer.toString(cotizacion.get(i).getProcodigo());
            dato[2] = cotizacion.get(i).getCce_fecha();
            
            dato[3] = Integer.toString(cotizacion2.get(i).getProdcodigo());//DETALLE
            dato[4] = Integer.toString(cotizacion2.get(i).getCce_cantidad_producto());
            dato[5] = Integer.toString(cotizacion2.get(i).getCce_total_producto());
            

            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

